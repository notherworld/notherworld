// shade a biome base by elevation: darker in the lowlands, lifting on the heights,
// so the terrain has relief without changing what the biome IS.
function biomeShade(base: RGB, e: number): RGB {
  const t = clamp01((e - SEA) / (1 - SEA));       // 0 at sea level → 1 at peak
  const f = 0.82 + t * 0.30;                        // 0.82 low → 1.12 high
  return [clampByte(base[0] * f), clampByte(base[1] * f), clampByte(base[2] * f)];
}

// SNOW with FORM — a crisp snowcap, not a flat blob. The summit is bright white; a
// cool blue-grey shadow lower down gives relief; a little ROCK pokes through near
// the snowline so it reads as snow lying ON a mountain, not a paint splat.
function snowShade(x: number, y: number, e: number, distToSea: number): RGB {
  const peak = smoothstep(SNOWLINE + 0.01, SNOWLINE + 0.11, e);   // 0 at snowline → 1 at the summit
  const lo: RGB = [190, 200, 216];                  // shadowed snow (blue-grey)
  const hi: RGB = [246, 249, 253];                  // sunlit summit
  let c: RGB = [lerp(lo[0], hi[0], peak), lerp(lo[1], hi[1], peak), lerp(lo[2], hi[2], peak)];
  const g = (h(x * 60013 ^ y * 52711, 0x5) - 0.5) * 9; // fine granular sparkle
  c = [clampByte(c[0] + g), clampByte(c[1] + g), clampByte(c[2] + g)];
  if (peak < 0.35 && h(x * 3299 ^ y * 7919, 0x2c) < 0.22 - peak * 0.5) {
    return mixToward(c, ROCK, 0.7);                 // exposed crag at the lower rim
  }
  void distToSea;
  return c;
}

// SPECKLE — a SUBTLE seeded grain so a big fill (rock, snow) reads as slightly
// broken rather than a flat slab. Kept gentle on purpose: strong jitter turns the
// whole area into TV static. Only occasional, faint darker crags. Deterministic.
function speckle(base: RGB, x: number, y: number, amp: number): RGB {
  const n = h(x * 374761393 ^ y * 668265263, 0x9e37) - 0.5; // [-0.5,0.5)
  const d = n * amp;
  const crag = h(x * 2246822519 ^ y * 3266489917, 0x85eb) < 0.06 ? -amp : 0; // rare, faint
  return [clampByte(base[0] + d + crag), clampByte(base[1] + d + crag), clampByte(base[2] + d + crag)];
}

// A district's identity HUE (0..360) — used ONLY as a faint wash on the natural
// land, and as the clear tint when the district is hovered. Not a fill.
function districtHue(d: EntityDto): number {
  const ring0 = (d.stats.ring ?? 1) < 0.5;
  const warmth = d.stats.warmth ?? 0.5;
  const ch = d.stats.character ?? ((d.id * 0.137) % 1);
  return ring0 ? 44 + warmth * 8 : (ch * 300 + warmth * 20) % 360;
}
// Wash a base terrain colour toward a district hue by `amount` (0 = pure terrain,
// 1 = full identity). A whisper at rest, a clear lift on hover.
function washToward(base: RGB, hue: number, amount: number, lift = 0): RGB {
  const [tr, tg, tb] = hsl(hue, 45, 52); // the identity colour at full strength
  return [
    clampByte(lerp(base[0], tr, amount) + lift),
    clampByte(lerp(base[1], tg, amount) + lift),
    clampByte(lerp(base[2], tb, amount) + lift),
  ];
}

