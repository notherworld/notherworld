// combinations, deterministic per world, different across worlds — seed 20's
// Dunhollow is not seed 22's anything. The CORE district isn't hardcoded
// "Downtown" either — each world calls its centre something of its own.
const NAME_A = ['Ash', 'Dun', 'Fen', 'Salt', 'Marrow', 'Bram', 'Kel', 'Thorn', 'Grim', 'Linden', 'Cinder', 'Hollow', 'Green', 'Stone', 'Wick', 'Harrow'];
const NAME_B = ['moor', 'hollow', 'bridge', 'mere', 'wick', 'gate', 'ford', 'reach', 'fen', 'holm', 'side', 'row', 'hill', 'march', 'stead', 'combe'];
const CORES = ['Downtown', 'Old Town', 'The Wharf', 'Midtown', 'The Crossing', 'Kingsyard', 'The Exchange', 'Harbour Core'];
function seededName(seed: number, id: number): string {
  const a = h(seed * 31 + id * 7, 0x11), b = h(seed * 17 + id * 13, 0x12);
  return NAME_A[(a * NAME_A.length) | 0] + NAME_B[(b * NAME_B.length) | 0];
}
function districtName(e: EntityDto, seed: number): string {
  if (WILD) return `sector ${e.id}`;                     // nobody named it — a chart reference
  return (e.stats.ring ?? 1) < 0.5 ? CORES[(h(seed * 41, 0x13) * CORES.length) | 0] : seededName(seed, e.id);
}
function cityName(seed: number): string {
  if (ARRIVAL && WILD) return ARRIVAL.name;              // the BODY's name is the only name here
  return seededName(seed * 101 + 3, 1);
}
// breadcrumb label for any scope on the dive trail
function crumbName(e: EntityDto, seed: number): string {
  if (e.kind === 'city') return cityName(seed);
  if (e.kind === 'district') return districtName(e, seed);
  if (WILD && e.kind === 'block') return `zone ${e.id}`;
  return e.name || `${e.kind} ${e.id}`;
}

function descriptor(e: EntityDto, ring0: boolean): string {
  const beach = e.stats.beach_frac ?? 0, cliff = e.stats.cliff_frac ?? 0;
  const shore = e.stats.shore_frac ?? 0, hill = e.stats.hill_frac ?? 0;
  if (WILD) {
    // NATURE reads the same coverage stats people would have settled by —
    // gather everything that's TRUE of this sector, then let its id pick, so
    // neighbouring sectors with similar coasts still read differently.
    const wooded = e.stats.wooded_frac ?? 0, rock = e.stats.rock_frac ?? 0, dry = e.stats.dry_frac ?? 0;
    const opts: string[] = [];
    if (cliff > 0.14) opts.push('sheer sea-cliffs');
    if (beach > 0.10) opts.push('long empty shores');
    if (shore > 0.10) opts.push('a silent coastline');
    if (hill > 0.45) opts.push('high broken ground');
    if (rock > 0.30) opts.push('fields of shattered rock');
    if (wooded > 0.25) opts.push('dense untouched growth');
    if (dry > 0.35) opts.push('open dust flats');
    if (opts.length === 0) opts.push('untouched expanse', 'a quiet emptiness', 'windswept wastes');
    return opts[(h(e.id * 13, 0x21) * opts.length) | 0];
  }
  if (beach > 0.10) return 'a breezy harbour town';
  if (cliff > 0.14) return 'a rugged clifftop quarter';
  if (shore > 0.10) return 'a working waterfront';
  if (hill > 0.45) return 'windswept hill streets';
  if (ring0) return 'the dense, humming core';
  const warmth = e.stats.warmth ?? 0.5, wealth = e.stats.wealth ?? 0.5, dens = e.stats.density ?? 0.5;
  if (warmth > 0.6) return 'a restless nightlife quarter';
  if (wealth > 0.66) return 'leafy, well-to-do streets';
  if (dens < 0.4) return 'quiet outskirts';
  if (wealth < 0.4) return 'a hard-working district';
  return 'an ordinary neighbourhood';
}

// ---- colour utils ----
function hsl(h: number, s: number, l: number): RGB {
  h = ((h % 360) + 360) % 360; s /= 100; l /= 100;
  const c = (1 - Math.abs(2 * l - 1)) * s;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = l - c / 2;
  let r = 0, g = 0, b = 0;
  if (h < 60) [r, g, b] = [c, x, 0];
  else if (h < 120) [r, g, b] = [x, c, 0];
  else if (h < 180) [r, g, b] = [0, c, x];
  else if (h < 240) [r, g, b] = [0, x, c];
  else if (h < 300) [r, g, b] = [x, 0, c];
  else [r, g, b] = [c, 0, x];
  return [Math.round((r + m) * 255), Math.round((g + m) * 255), Math.round((b + m) * 255)];
}
// design/creature.ts emits CSS `hsl(H,S%,L%)` strings; the terra bake writes raw
// RGB bytes. This memoized parser bridges the two so drawSilhouette can stamp into
// the ImageData buffer. Tiny cache — a critter uses only 2 colours (body + dark).
const _hslCache = new Map<string, RGB>();
function hslStrToRgb(s: string): RGB {
  const hit = _hslCache.get(s);
  if (hit) return hit;
  const m = /hsl\(\s*([\d.]+)\s*,\s*([\d.]+)%\s*,\s*([\d.]+)%\s*\)/.exec(s);
  const rgb: RGB = m ? hsl(+m[1], +m[2], +m[3]) : [200, 120, 120];
  _hslCache.set(s, rgb);
  return rgb;
}
function clamp01(v: number): number { return v < 0 ? 0 : v > 1 ? 1 : v; }
function lerp(a: number, b: number, t: number): number { return a + (b - a) * t; }
function clampByte(v: number): number { return v < 0 ? 0 : v > 255 ? 255 : v | 0; }

// ─── ROOFS ───────────────────────────────────────────────────────────────────
// The building footprint(s) as CELL SETS, carved from the lot's own region cells
// (`world.region`). This is the SAME shape logic the city demo proved — coverage-
// driven erosion, wall smoothing, slenderness cap, split — kept verbatim because
// it's pure cell math (no SVG). Atlas draws the RESULT as pixel-art volume, not a
// flat SVG fill: that's where it beats city. See BlockMap.roofGroups for the why
// behind each step (the bugs paid for: coverage-driven erosion, no bbox margin;
// loop min/max, never Math.max(...huge)).
function roofGroups(flat: number[], fill: number, split: number): GCell[][] {
  const cell = flat[0];
  const key = (gx: number, gy: number) => `${gx},${gy}`;
  const cells: GCell[] = [];
  const own = new Set<string>();
  for (let i = 1; i + 1 < flat.length; i += 2) {
    const gx = Math.round(flat[i] / cell - 0.5), gy = Math.round(flat[i + 1] / cell - 0.5);
    cells.push({ gx, gy }); own.add(key(gx, gy));
  }
  if (!cells.length) return [];
  // COVERAGE-DRIVEN erosion: peel boundary rings until survivors ≈ fill×total,
  // stopping BEFORE a pass that would overshoot/empty. Exact for any lot shape.
  const target = Math.max(4, Math.round(cells.length * Math.max(0.05, Math.min(1, fill))));
  let alive = new Set(own);
  // PASS CAP (perf): each pass is O(cells); Atlas lots eroded to a small cap-fill can
  // want dozens of rings, and this runs per building per re-render. 16 passes is more
  // erosion than any footprint visually needs — beyond that, stop where we are.
  let passes = 0;
  while (alive.size > target && passes < 16) {
    passes++;
    const next = new Set<string>();
    for (const c of cells) {
      if (!alive.has(key(c.gx, c.gy))) continue;
      const inner = alive.has(key(c.gx - 1, c.gy)) && alive.has(key(c.gx + 1, c.gy))
        && alive.has(key(c.gx, c.gy - 1)) && alive.has(key(c.gx, c.gy + 1));
      if (inner) next.add(key(c.gx, c.gy));
    }
    if (next.size === 0 || next.size < target * 0.6) break;
    alive = next;
  }
  let roof = cells.filter((c) => alive.has(key(c.gx, c.gy)));
  if (!roof.length) roof = cells;
  // straighten walls: fill concave notches (≥3 roof nbrs), shave spurs (≤1)
  if (roof.length > 4) {
    let set = new Set(roof.map((c) => key(c.gx, c.gy)));
    const nOf = (s: Set<string>, gx: number, gy: number) =>
      Number(s.has(key(gx - 1, gy))) + Number(s.has(key(gx + 1, gy)))
      + Number(s.has(key(gx, gy - 1))) + Number(s.has(key(gx, gy + 1)));
    for (let pass = 0; pass < 2; pass++) {
      const next = new Set(set);
      for (const c of cells) if (!set.has(key(c.gx, c.gy)) && nOf(set, c.gx, c.gy) >= 3) next.add(key(c.gx, c.gy));
      for (const c of cells) if (next.has(key(c.gx, c.gy)) && nOf(next, c.gx, c.gy) <= 1 && next.size > 4) next.delete(key(c.gx, c.gy));
      set = next;
    }
    roof = cells.filter((c) => set.has(key(c.gx, c.gy)));
    if (!roof.length) roof = cells;
  }
  // max slenderness ~3:1 (a building is a chunk, not a ribbon)
  {
    let bx0 = Infinity, bx1 = -Infinity, by0 = Infinity, by1 = -Infinity, cxm = 0, cym = 0;
    for (const c of roof) {
      if (c.gx < bx0) bx0 = c.gx; if (c.gx > bx1) bx1 = c.gx;
      if (c.gy < by0) by0 = c.gy; if (c.gy > by1) by1 = c.gy;
      cxm += c.gx; cym += c.gy;
    }
    cxm /= roof.length; cym /= roof.length;
    const bw = bx1 - bx0 + 1, bh = by1 - by0 + 1;
    const short = Math.min(bw, bh), long = Math.max(bw, bh);
    const maxLong = Math.max(3, short * 3) * Math.max(1, split);
    if (long > maxLong) {
      const alongX = bw >= bh;
      const ctr = alongX ? cxm : cym;
      const lo = Math.round(ctr - maxLong / 2), hi = lo + maxLong;
      const cut = roof.filter((c) => { const v = alongX ? c.gx : c.gy; return v >= lo && v < hi; });
      if (cut.length) roof = cut;
    }
  }
  if (split <= 1) return [roof];
  // partition along the longer axis into `split` bands with a scaled gap
  let rx0 = Infinity, rx1 = -Infinity, ry0 = Infinity, ry1 = -Infinity;
  for (const c of roof) {
    if (c.gx < rx0) rx0 = c.gx; if (c.gx > rx1) rx1 = c.gx;
    if (c.gy < ry0) ry0 = c.gy; if (c.gy > ry1) ry1 = c.gy;
  }
  const rw = rx1 - rx0 + 1, rh = ry1 - ry0 + 1;
  const alongX = rw >= rh;
  const lo = alongX ? rx0 : ry0;
  const extent = alongX ? rw : rh;
  const n = Math.min(split, Math.max(1, Math.floor((extent + 1) / 2)));
  const groups: GCell[][] = Array.from({ length: n }, () => []);
  const gapW = Math.max(1, Math.round((extent / n) * 0.12));
  for (const c of roof) {
    const v = (alongX ? c.gx : c.gy) - lo;
    const gi = Math.min(n - 1, Math.floor((v / extent) * n));
    const bandLo = Math.ceil((gi * extent) / n);
    if (gi > 0 && v < bandLo + gapW) continue;
    groups[gi].push(c);
  }
  return groups.filter((g) => g.length > 0);
}

// ROOF MATERIALS — believable roofing, NOT a function legend. A game building's
// TYPE is read from ARCHITECTURE (pitched vs flat, size, chimney, rooftop plant,
// a shop's sign), never from a colour-coded slab. So every roof draws from the
// same real material family — slate greys, shingle browns, dark metal, tar — and
// the use only picks WHICH material + WHICH form, the way real buildings differ.
// [base, sunlit, shadow]. All mid-dark & low-saturation: a town, not a candy box.
// Muted, desaturated roofing — reads as weathered material at map scale, not a bold
// slab. Low chroma, mid-dark; the hi/lo give facet shading without going glossy.
type RoofMat = { base: RGB; hi: RGB; lo: RGB };
const MAT_SLATE:  RoofMat = { base: [82, 86, 94],   hi: [112, 116, 124], lo: [52, 55, 62] };  // grey slate — homes
const MAT_SHINGLE:RoofMat = { base: [96, 80, 68],   hi: [128, 110, 94],  lo: [60, 48, 40] };  // brown shingle — homes/café
const MAT_TILE:   RoofMat = { base: [112, 82, 66],  hi: [146, 110, 90],  lo: [70, 48, 38] };  // clay tile — shops (muted)
const MAT_METAL:  RoofMat = { base: [86, 90, 98],   hi: [118, 122, 130], lo: [54, 57, 64] };  // metal seam — civic/office
const MAT_TAR:    RoofMat = { base: [72, 74, 79],   hi: [100, 102, 108], lo: [46, 47, 51] };  // tar/gravel deck — big flat
// use (0 resi 1 shop 2 café 3 clinic 4 school 5 office) → material + form. Homes &
