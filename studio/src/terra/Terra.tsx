// Rasterise a polyline into grid cells (Bresenham per segment), calling `plot`
// for each cell. `t` counts cells along the whole line (for dashing). `thick`
// Rasterise a polyline into a CONTINUOUS run of grid cells. `rad` = brush radius
// (0 = 1px, 1 = 3px round-ish, …). Two fixes for "roads that don't touch":
//  1) a real brush width (rad ≥ 1) so a road reads as a road from far away, and
//  2) 4-CONNECTED stepping — on a diagonal Bresenham move (both x and y change),
//     we also fill the intervening orthogonal cell, so the line never leaves a
//     corner-only gap. `plot` gets a running cell index `t` (for dashing).
function rasterPolyline(pts: P[], rad: number, plot: (gx: number, gy: number, t: number) => void) {
  let t = 0;
  const brush = (cx: number, cy: number) => {
    if (rad <= 0) { plot(cx, cy, t); return; }
    if (rad === 0.5) {                                // 2-px thick line (2×2 block)
      plot(cx, cy, t); plot(cx + 1, cy, t); plot(cx, cy + 1, t); plot(cx + 1, cy + 1, t);
      return;
    }
    const ri = Math.round(rad);
    for (let oy = -ri; oy <= ri; oy++) for (let ox = -ri; ox <= ri; ox++)
      if (ox * ox + oy * oy <= ri * ri + ri) plot(cx + ox, cy + oy, t); // round-ish disc
  };
  for (let s = 0; s < pts.length - 1; s++) {
    let x0 = Math.round(pts[s].x), y0 = Math.round(pts[s].y);
    const x1 = Math.round(pts[s + 1].x), y1 = Math.round(pts[s + 1].y);
    const dx = Math.abs(x1 - x0), dy = -Math.abs(y1 - y0);
    const sx = x0 < x1 ? 1 : -1, sy = y0 < y1 ? 1 : -1;
    let er = dx + dy;
    for (;;) {
      brush(x0, y0);
      t++;
      if (x0 === x1 && y0 === y1) break;
      const e2 = 2 * er;
      const stepX = e2 >= dy, stepY = e2 <= dx;
      if (stepX) { er += dy; x0 += sx; }
      if (stepY) { er += dx; y0 += sy; }
      // if we moved diagonally, drop an orthogonal filler so the run stays touching
      if (stepX && stepY) brush(x0, y0 - sy);
    }
  }
}

