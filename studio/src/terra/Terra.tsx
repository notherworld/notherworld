function mixToward(a: RGB, b: RGB, t: number): RGB {
  return [clampByte(lerp(a[0], b[0], t)), clampByte(lerp(a[1], b[1], t)), clampByte(lerp(a[2], b[2], t))];
}
// smooth 0→1 ramp between edges e0 and e1 (e0 may be > e1 to invert). The core of
// biome FALLOFF — replaces hard >/< thresholds so regions feather into each other.
function smoothstep(e0: number, e1: number, x: number): number {
  const t = clamp01((x - e0) / (e1 - e0));
  return t * t * (3 - 2 * t);
}
// CLUSTERED + NOISE-JITTERED dither → a per-pixel threshold in (0,1). A pure
// ordered matrix (Bayer OR clustered) TILES, so the eye catches the repeat. We
// keep the clustered matrix for the clumping CHARACTER but perturb it with a
// deterministic per-pixel hash, so the clumps land irregularly — organic, no
// visible grid, every pixel still a solid colour. JITTER = how much noise vs
// how much ordered structure.
const CLUSTER6 = [
  34, 29, 17, 21, 30, 35,
  28, 14,  9, 16, 20, 31,
  13,  8,  4,  5, 15, 19,
  12,  3,  0,  1, 10, 18,
  27, 11,  2,  6, 23, 24,
  33, 26,  7, 22, 25, 32,
];
const JITTER = 0.55; // 0 = pure ordered (tiles), 1 = pure noise (fizzy). ~0.5 = organic clumps
function bayer(x: number, y: number): number {
  const ordered = clusterDither(x, y);
  const noise = h(x * 73856093 ^ y * 19349663, 0x51ed); // deterministic per-pixel [0,1)
  return clamp01(ordered * (1 - JITTER) + noise * JITTER);
}
// VALUE NOISE — smooth pseudo-random field from the hash, bilinearly interpolated
// so it varies continuously in x/y (and drifts when you offset the coords). Used
// to turbulate the clouds into wisps. Cheap, deterministic.
function vnoise(x: number, y: number): number {
  const xi = Math.floor(x), yi = Math.floor(y);
  const tx = x - xi, ty = y - yi;
  const sx = tx * tx * (3 - 2 * tx), sy = ty * ty * (3 - 2 * ty); // smoothstep fade
  const c00 = h((xi & 8191) * 131 ^ (yi & 8191) * 977, 0x11);
  const c10 = h(((xi + 1) & 8191) * 131 ^ (yi & 8191) * 977, 0x11);
  const c01 = h((xi & 8191) * 131 ^ ((yi + 1) & 8191) * 977, 0x11);
  const c11 = h(((xi + 1) & 8191) * 131 ^ ((yi + 1) & 8191) * 977, 0x11);
  return lerp(lerp(c00, c10, sx), lerp(c01, c11, sx), sy);
}
// the pure clustered matrix (no noise) — tight organic clumps, used for foam.
function clusterDither(x: number, y: number): number {
  return (CLUSTER6[(((y % 6) + 6) % 6) * 6 + (((x % 6) + 6) % 6)] + 0.5) / 36;
}
