// DAY/NIGHT GRADE — a full-screen multiply tint driven by the sim's `hour` (the SAME
// clock the people read for their schedules — one truth). Midday is near-white (no
// change); night darkens to deep blue; dawn/dusk warm to orange. Interpolated between
// keyframes and CSS-eased, so the whole city visibly slides through its day.
const GRADE_KEYS: [number, [number, number, number]][] = [
  [0,  [64, 78, 132]], [5, [92, 100, 152]], [7, [255, 202, 168]], [10, [255, 253, 248]],
  [15, [255, 253, 248]], [18, [255, 212, 176]], [20, [252, 158, 118]], [22, [112, 96, 142]], [24, [64, 78, 132]],
];
function hourGrade(h: number): string {
  h = ((h % 24) + 24) % 24;
  let a = GRADE_KEYS[0], b = GRADE_KEYS[GRADE_KEYS.length - 1];
  for (let i = 0; i < GRADE_KEYS.length - 1; i++) {
    if (h >= GRADE_KEYS[i][0] && h <= GRADE_KEYS[i + 1][0]) { a = GRADE_KEYS[i]; b = GRADE_KEYS[i + 1]; break; }
  }
  const t = (h - a[0]) / Math.max(0.001, b[0] - a[0]);
  const c = a[1].map((v, i) => Math.round(v + (b[1][i] - v) * t));
  return `rgb(${c[0]}, ${c[1]}, ${c[2]})`;
}

